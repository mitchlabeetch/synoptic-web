// ═══════════════════════════════════════════
// UTILITY FUNCTIONS
// ═══════════════════════════════════════════

export function getLanguageByCode(code: string): LanguageConfig | undefined {
  return SUPPORTED_LANGUAGES.find((lang) => lang.code === code);
}

export function getLanguageLabel(code: string, inEnglish = false): string {
  const lang = getLanguageByCode(code);
  if (!lang) return code;
  return inEnglish ? lang.labelEn : lang.label;
}

export function isRTL(code: string): boolean {
  const lang = getLanguageByCode(code);
  return lang?.direction === 'rtl';
}

export function getDefaultFont(code: string): string {
  const lang = getLanguageByCode(code);
  return lang?.suggestedFonts[0] || 'Inter';
}

export function requiresSpecialRendering(code: string): boolean {
  const lang = getLanguageByCode(code);
  return lang?.requiresSpecialRendering || false;
}
