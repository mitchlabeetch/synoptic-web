export function getTileById(id: string): LibraryTile | undefined {
  return LIBRARY_TILES.find(tile => tile.id === id);
}

export function getTilesByCategory(category: LibraryTile['category']): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.category === category);
}

export function getTilesBySourceId(sourceId: string): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.sourceId === sourceId);
}

export function getCommercialSafeTiles(): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.license.type === 'commercial-safe');
}

export function getAttributionTiles(): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.license.type === 'attribution');
}

export function getPersonalOnlyTiles(): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.license.type === 'personal-only');
}

export function searchTiles(query: string): LibraryTile[] {
  const q = query.toLowerCase();
  return LIBRARY_TILES.filter(tile =>
    tile.title.toLowerCase().includes(q) ||
    tile.subtitle.toLowerCase().includes(q) ||
    tile.tags.some(tag => tag.includes(q)) ||
    tile.description?.toLowerCase().includes(q)
  );
}

export function getFeaturedTiles(): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.size === 'lg');
}

export function getTilesByDifficulty(difficulty: 'beginner' | 'intermediate' | 'expert'): LibraryTile[] {
  return LIBRARY_TILES.filter(tile => tile.difficulty === difficulty);
}

export function getTileStats() {
  return {
    total: LIBRARY_TILES.length,
    commercial: getCommercialSafeTiles().length,
    attribution: getAttributionTiles().length,
    personal: getPersonalOnlyTiles().length,
    categories: [...new Set(LIBRARY_TILES.map(t => t.category))].length,
    sources: [...new Set(LIBRARY_TILES.map(t => t.sourceId))].length,
  };
}
